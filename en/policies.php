Our Quality Policy is based on:

- Ensuring customer satisfaction by delivering the best cargo transportation service solutions for each of their specific needs, with high-quality standards and the best delivery times, with real-time tracking.

- Periodically reviewing our internal processes to continuously improve the management of our system's risks.

- Ensuring the quality of our services through the technical competencies and extensive professional experience of our staff, as well as compliance with current legal and regulatory requirements.

- Maintaining truthful, clear, and timely communication and information with our employees, customers, suppliers, the community, authorities, and other stakeholders.

TRANSPORTES NORTE LTDA's Quality Policy will be communicated and made available to all Organization Stakeholders. It will also be subject to reviews and updates as required during the implementation and adaptation of the Quality Management System.
