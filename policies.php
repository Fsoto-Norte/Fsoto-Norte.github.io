                   <b style="font-size:23px;">POLÍTICA DE LA CALIDAD</b><br>
				   <br>

TRANSPORTES NORTE LTDA, es una empresa con prestación de servicios especializados en Transporte Nacional de Carga y Logística asociada, con base en Santiago de Chile, además cuenta con una amplia Red Nacional de Agentes y Representantes; nuestro modelo de negocios nos permite seleccionar para cada tipo de cliente las mejores opciones dentro de una amplia gama de equipo para así optimizar los niveles de servicio y maximizar los resultados económicos de cada negocio.
Nuestra exitosa trayectoria y expertise nos avalan como una empresa de servicios especializada, confiable y competitiva, la que junto a sistema operativos y un equipo de profesionales de alta calificación nos permiten entregar a todos nuestros clientes soluciones efectivas y de calidad las cuales permiten satisfacer sus necesidades y expectativas buscando siempre la mejora continua de los procesos.

Nuestra política de la Calidad está basada en: 
-	Garantizar la satisfacción de nuestros clientes a través de la entrega de las mejores soluciones de servicios de transporte de carga, para cada una de sus necesidades específicas, con altos estándares de calidad y en los mejores plazos de entrega con seguimiento en tiempo real.

-	Revisar periódicamente nuestros procesos internos con el fin de mejorar continuamente la gestión de los riesgos de nuestro sistema.

-	Asegurar la calidad de nuestros servicios a través de las competencias técnicas y amplia experiencia profesional de nuestro personal y cumplimiento de los requisitos legales y reglamentarios vigentes.

-	Mantener una comunicación e información fidedigna, clara y oportuna con nuestros trabajadores, clientes, proveedores, la comunidad, autoridad y demás partes interesadas.

La política de la Calidad de TRANSPORTES NORTE LTDA, será comunicada y estará a disposición de todas las Partes Interesadas de la Organización. Asimismo, estará sujeta a las revisiones y actualizaciones que requiera durante la ejecución y adecuación del Sistema de Gestión de la Calidad. <br>
