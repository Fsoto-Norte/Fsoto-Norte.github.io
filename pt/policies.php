                   <b style="font-size:23px;">POLÍTICA DE QUALIDADE</b><br>
				   <br>
TRANSPORTES NORTE LTDA é uma empresa que oferece serviços especializados em Transporte Nacional de Carga e Logística associada, com sede em Santiago, Chile. Além disso, contamos com uma ampla Rede Nacional de Agentes e Representantes. Nosso modelo de negócios nos permite selecionar as melhores opções de equipamentos para cada tipo de cliente, a fim de otimizar os níveis de serviço e maximizar os resultados econômicos de cada negócio.
Nossa bem-sucedida trajetória e experiência nos credenciam como uma empresa de serviços especializados, confiável e competitiva. Em conjunto com sistemas operacionais e uma equipe de profissionais altamente qualificados, somos capazes de oferecer soluções eficazes e de qualidade a todos os nossos clientes, que atendem às suas necessidades e expectativas, buscando sempre a melhoria contínua dos processos.

Nossa política de Qualidade se baseia em:

- Garantir a satisfação de nossos clientes através da entrega das melhores soluções em serviços de transporte de carga, atendendo a cada uma de suas necessidades específicas, com altos padrões de qualidade e nos melhores prazos de entrega, com acompanhamento em tempo real.

- Revisar periodicamente nossos processos internos a fim de melhorar continuamente a gestão dos riscos do nosso sistema.

- Assegurar a qualidade de nossos serviços através das competências técnicas e ampla experiência profissional de nossa equipe, bem como o cumprimento dos requisitos legais e regulatórios vigentes.

- Manter uma comunicação e informação verdadeira, clara e oportuna com nossos colaboradores, clientes, fornecedores, comunidade, autoridades e demais partes interessadas.

A política de Qualidade da TRANSPORTES NORTE LTDA será comunicada e estará à disposição de todas as Partes Interessadas da Organização. Além disso, estará sujeita a revisões e atualizações conforme necessário durante a implementação e adequação do Sistema de Gestão da Qualidade.
